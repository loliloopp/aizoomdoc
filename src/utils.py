import re

def transliterate(text: str) -> str:
    """
    Транслитерация кириллицы в латиницу и очистка строки.
    Например: "Кинг 12" -> "king_12"
    """
    mapping = {
        'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'yo',
        'ж': 'zh', 'з': 'z', 'и': 'i', 'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm',
        'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't', 'у': 'u',
        'ф': 'f', 'х': 'kh', 'ц': 'ts', 'ч': 'ch', 'ш': 'sh', 'щ': 'shch',
        'ъ': '', 'ы': 'y', 'ь': '', 'э': 'e', 'ю': 'yu', 'я': 'ya'
    }
    
    # В нижний регистр
    text = text.lower()
    
    # Замена кириллицы
    result = []
    for char in text:
        result.append(mapping.get(char, char))
    
    text = "".join(result)
    
    # Замена пробелов и спецсимволов на подчеркивание
    text = re.sub(r'[\s\-_]+', '_', text)
    
    # Удаление всего кроме букв, цифр и подчеркивания
    text = re.sub(r'[^\w]', '', text)
    
    # Удаление лишних подчеркиваний по краям
    text = text.strip('_')
    
    return text

